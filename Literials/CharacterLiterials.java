package Literials;
public class CharacterLiterials{
    public static void main(String args[]){
        System.out.println('a');
        
        System.out.println("->");
        System.out.println('\n');
        System.out.println("<-");

        System.out.println("->");
        System.out.println('\t');
        System.out.println("<-");

        System.out.println("->");
        System.out.println('\\');
        System.out.println("<-");

        System.out.println("->");
        System.out.println('\'');
        System.out.println("<-");

        System.out.println('\u0041');

    }
}