public class ifElseExample{
    public static void main(String args[]){
        int score = 82;
        if(score > 80){
            System.out.println("Excellent");
        }else if(score > 60){
            System.out.println("first class");
        }else if(score > 40){
            System.out.println("Second Class");
        }else {
            System.out.println("Fail");
        }
    }
}


