package Primitives;
public class BytePrimitives{
    public static void main(String args[]){
        byte b1 = 25;
        byte b2 = 0b10010;
        byte b3 = 0x19;
        byte b4 = 031;
        System.out.println(b1);
        System.out.println(b2);
        System.out.println(b3);
        System.out.println(b4);
    }
}