package Primitives;
public class FloatAndDouble{
    public static void main(String args[]){
        float f1 = 2.718F;
        System.out.println(f1);
        float f2 = 271e-3F;
        System.out.println(f2);
        double d1 = 125.4E4;
        System.out.println(d1);
        double d2 = 3.12564;
        System.out.println(d2);
    }
}