package Arrays;
public class findAverage {
    public static void main(String args[]) {
        int score1 = 60;
        int score2 = 50;
        int score3 = 55;
        int score4 = 80;

        int avgScore = (score1 + score2 + score3 + score4) / 4;
        System.out.println("Average of score : " + avgScore);
    }
}